BEGIN
	security.user_pkg.logonadmin(:bv_site_name);
	
	cms.tab_pkg.RegisterTable(
		in_oracle_schema => 'RAG',
		in_oracle_table => 'SAP_COMPANY_STAGING,BSCI_COMPANY_STAGING,TERTIARY_COMPANY_STAGING',
		in_managed => FALSE
