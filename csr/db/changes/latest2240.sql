create or replace procedure csrimp.droppk(
	in_owner			IN	VARCHAR2,
	in_table_name		IN	VARCHAR2
)
authid current_user
as
	v_sql varchar2(4000);
begin
	--dbms_output.put_line('droppk('||in_owner||'.'||in_table_name||');');
	for r in (
		select owner, table_name, constraint_name, constraint_type
		  from all_constraints
		 where owner = in_owner AND table_name = in_table_name and constraint_type = 'P'
	) loop
		v_sql := 'alter table '||r.owner||'.'||r.table_name||' drop constraint '||r.constraint_name;
		if r.constraint_type in ('P', 'U') then
			v_sql := v_sql||' drop index';
		end if;
		dbms_output.put_line(v_sql);
		execute immediate(v_sql);
	end loop;
end;
