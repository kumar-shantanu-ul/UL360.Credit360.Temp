CREATE TABLE CSR.BRANDING(
	CLIENT_FOLDER_NAME          VARCHAR(255) 	NOT NULL,
	BRANDING_TITLE   			VARCHAR(100)  NOT NULL,
	AUTHOR    				    VARCHAR(100)  DEFAULT NULL,
	CONSTRAINT PK_BRANDING PRIMARY KEY (CLIENT_FOLDER_NAME)
);

CREATE TABLE CSR.BRANDING_AVAILABILITY(
	APP_SID         			NUMBER(10, 0)    DEFAULT SYS_CONTEXT('SECURITY','APP') NOT NULL,
	CLIENT_FOLDER_NAME 		 	VARCHAR(255)	 NOT NULL,
	CONSTRAINT PK_BRANDING_AVAILABILITY PRIMARY KEY (APP_SID, CLIENT_FOLDER_NAME),
	CONSTRAINT FK_BRANDING_CLIENT_FOLDER_NAME FOREIGN KEY (CLIENT_FOLDER_NAME) REFERENCES CSR.BRANDING (CLIENT_FOLDER_NAME)
);


CREATE TABLE CSR.BRANDING_LOCK(
	APP_SID                     NUMBER(10, 0)    DEFAULT SYS_CONTEXT('SECURITY','APP') NOT NULL,
	LOCK_EXPIRY_DTM             DATE             NOT NULL,
	LOCKED_BY                   NUMBER(10, 0)    NOT NULL,
	UNLOCKED_BY                 NUMBER(10, 0)    NULL,
	UNLOCKED_DTM                DATE             NULL,
	CONSTRAINT PK_BRANDING_LOCK PRIMARY KEY (APP_SID, LOCK_EXPIRY_DTM)
);

DECLARE
    FEATURE_NOT_ENABLED EXCEPTION;
    PRAGMA EXCEPTION_INIT(FEATURE_NOT_ENABLED, -439);
    POLICY_ALREADY_EXISTS EXCEPTION;
    PRAGMA EXCEPTION_INIT(POLICY_ALREADY_EXISTS, -28101);
    TYPE T_TABS IS TABLE OF VARCHAR2(30);
    v_list T_TABS;
BEGIN
    v_list := t_tabs(
        'BRANDING_AVAILABILITY',
        'BRANDING_LOCK'
    );
    FOR I IN 1 .. v_list.count
    LOOP
        BEGIN
            DBMS_RLS.ADD_POLICY(
                object_schema   => 'CSR',
                object_name     => v_list(i),
                policy_name     => SUBSTR(v_list(i), 1, 23)||'_POLICY',
                function_schema => 'CSR',
                policy_function => 'appSidCheck',
                statement_types => 'select, insert, update, delete',
                update_check    => true,
                policy_type     => dbms_rls.context_sensitive );
                DBMS_OUTPUT.PUT_LINE('Policy added to '||v_list(i));
        EXCEPTION
            WHEN POLICY_ALREADY_EXISTS THEN
                DBMS_OUTPUT.PUT_LINE('Policy exists for '||v_list(i));
            WHEN FEATURE_NOT_ENABLED THEN
                DBMS_OUTPUT.PUT_LINE('RLS policies not applied for '||v_list(i)||' as feature not enabled');
        END;
    END LOOP;
END;
