CREATE TABLE PRODUCT_IMPORT_MAPPING(
    IMPORT_PRODUCT_ID    NUMBER(10, 0)    NOT NULL,
    NEW_PRODUCT_ID       NUMBER(10, 0)    NOT NULL,
    DTM                  DATE             NOT NULL
)
;





-- 
-- TABLE: WOOD_IMPORT_DATA 
--

CREATE TABLE WOOD_IMPORT_DATA(
    PRODUCT_ID           NUMBER(10, 0)     NOT NULL,
    WOOD_NUMBER          NUMBER(10, 0)     NOT NULL,
    PART_NAME            VARCHAR2(1024)    NOT NULL,
    QTY                  NUMBER(10, 2)     NOT NULL,
    WEIGHT               NUMBER(10, 2)     NOT NULL,
    WOOD_TYPE            VARCHAR2(256)     NOT NULL,
    COUNTRY_OF_ORIGIN    VARCHAR2(256)     NOT NULL,
    CERT_TYPE            VARCHAR2(3)       NOT NULL,
    CERT_SCHEME          VARCHAR2(256)     NOT NULL
)
;



