

-- 
-- TABLE: PRODUCT_IMPORT_DATA 
--

CREATE TABLE PRODUCT_IMPORT_DATA(
    PRODUCT_ID           NUMBER(10, 0)     NOT NULL,
    ITEM_CODE            VARCHAR2(256)     NOT NULL,
    PRODUCT              VARCHAR2(1024)    NOT NULL,
    BUSINESS_CENTRE      VARCHAR2(256),
    BUSINESS_CENTRE_2    VARCHAR2(256),
    SUPPLIER             VARCHAR2(256)     NOT NULL,
    PRODUCT_TYPE         VARCHAR2(256),
    BRAND                VARCHAR2(256),
    WRME                 NUMBER(10, 2),
    SPLIT_WRME           NUMBER(10, 2),
    CONSTRAINT PRODUCT_IMPORT_DATA_PK PRIMARY KEY (PRODUCT_ID)
    USING INDEX
TABLESPACE INDX
)
;



-- 
-- TABLE: PRODUCT_IMPORT_MAPPING 
