
CREATE GLOBAL TEMPORARY TABLE SUPPLIER.TEMP_COMPANY
(
  COMPANY_SID  NUMBER(22,10)                    NOT NULL,
  NAME         VARCHAR2(256 BYTE)               NOT NULL,
  DELETED      NUMBER(22,1)                     NOT NULL
)
ON COMMIT DELETE ROWS
NOCACHE;


CREATE UNIQUE INDEX SUPPLIER.TEMP_COMPANY_PK ON SUPPLIER.TEMP_COMPANY
(COMPANY_SID);


ALTER TABLE SUPPLIER.TEMP_COMPANY ADD (
  CONSTRAINT TEMP_COMPANY_PK
 PRIMARY KEY
 (COMPANY_SID));


