CREATE GLOBAL TEMPORARY TABLE ACTIONS.AGGREGATE_TASKS
(
	LVL					NUMBER(10, 0)	NOT NULL,
	TASK_SID			NUMBER(10, 0)	NOT NULL,
	TASK_START_DTM		DATE			NOT NULL,	
	TASK_END_DTM		DATE			NOT NULL,
	TASK_INTERVAL		NUMBER(10, 0)	NOT NULL,
	REGION_SID			NUMBER(10, 0)	NOT NULL,
	IND_SID				NUMBER(10, 0)	NOT NULL,
	PERIOD_START_DTM	DATE			NOT NULL,
	PERIOD_END_DTM		DATE			NOT NULL,
	PARENT_REGION_SID	NUMBER(10, 0) 	NULL
)
ON COMMIT DELETE ROWS;

CREATE GLOBAL TEMPORARY TABLE ACTIONS.INITIATIVE_PROPERTIES
(
	REGION_SID			NUMBER(10, 0)	NOT NULL,
	REGION_DESC			VARCHAR2(1024)	NOT NULL,
	COUNTRY_SID			NUMBER(10, 0)	NOT NULL,
	COUNTRY_DESC		VARCHAR2(1024)	NOT NULL,
	PROPERTY_SID		NUMBER(10, 0)	NULL,
	PROPERTY_DESC		VARCHAR2(1024)	NULL
)
ON COMMIT DELETE ROWS;

CREATE GLOBAL TEMPORARY TABLE ACTIONS.PROGRESS_DATA
(
	IDX					NUMBER(10, 0)	NOT NULL,
	REGION_SID			NUMBER(10, 0)	NULL,
	IND_SID				NUMBER(10, 0)	NULL,
	PERIOD_START_DTM	DATE			NULL,
	VAL					NUMBER(24,10)	NULL
)
ON COMMIT DELETE ROWS;

CREATE GLOBAL TEMPORARY TABLE ACTIONS.TEMP_TASK_SIDS
(
	TASK_SID 			NUMBER(10, 0)	NOT NULL
)
ON COMMIT DELETE ROWS;

CREATE GLOBAL TEMPORARY TABLE ACTIONS.TEMP_CALC(
	XML 				   SYS.XMLType,
	STORED_CALC            NUMBER(1, 0)      DEFAULT 0 NOT NULL
                           CHECK (STORED_CALC IN(0,1))
)
ON COMMIT DELETE ROWS;
