begin
	for r in (select type_name from all_types where owner = 'DONATIONS' and type_name in (
				'T_SCHEME_TABLE')) loop
		--dbms_output.put_line('drop '||r.type_name);
		execute immediate 'DROP TYPE DONATIONS.'||r.type_name;
	end loop;
end;
/

CREATE OR REPLACE TYPE DONATIONS.T_SCHEME_ROW AS OBJECT ( 
	SCHEME_SID		NUMBER(10)
  );
/

CREATE OR REPLACE TYPE DONATIONS.T_SCHEME_TABLE AS TABLE OF DONATIONS.T_SCHEME_ROW;
/

CREATE OR REPLACE TYPE DONATIONS.T_POS_AND_DESCRIPTION_ROW AS OBJECT (
	POS NUMBER(10,0),	
	DESCRIPTION VARCHAR2(2047)
);
/

CREATE OR REPLACE TYPE DONATIONS.T_BUDGET_ID_ROW AS OBJECT (
	BUDGET_ID		NUMBER(10),
	CAN_VIEW_ALL	NUMBER(1),
	CAN_VIEW_MINE	NUMBER(1),
	CAN_VIEW_REGION	NUMBER(1)
);
/

CREATE OR REPLACE TYPE DONATIONS.T_BUDGET_ID_TABLE AS TABLE OF DONATIONS.T_BUDGET_ID_ROW;
/

-- has column called "column_value"
CREATE OR REPLACE TYPE DONATIONS.T_BUDGET_NAME_TABLE AS TABLE OF VARCHAR2(255);
/



